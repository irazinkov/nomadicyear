import { readFileSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const projectRoot = resolve(__dirname, "..", "..");

const csvPath = resolve(projectRoot, "docs/redirects.csv");
const redirectsOutPath = resolve(projectRoot, "public/_redirects");
const reportJsonPath = resolve(projectRoot, "docs/phase-5-redirect-report.json");
const reportMdPath = resolve(projectRoot, "docs/phase-5-redirect-report.md");

function parseCsv(text) {
  const rows = [];
  let row = [];
  let cell = "";
  let inQuotes = false;

  for (let i = 0; i < text.length; i += 1) {
    const char = text[i];
    const next = text[i + 1];

    if (char === '"') {
      if (inQuotes && next === '"') {
        cell += '"';
        i += 1;
      } else {
        inQuotes = !inQuotes;
      }
      continue;
    }

    if (char === "," && !inQuotes) {
      row.push(cell);
      cell = "";
      continue;
    }

    if ((char === "\n" || char === "\r") && !inQuotes) {
      if (char === "\r" && next === "\n") {
        i += 1;
      }
      row.push(cell);
      rows.push(row);
      row = [];
      cell = "";
      continue;
    }

    cell += char;
  }

  if (cell.length > 0 || row.length > 0) {
    row.push(cell);
    rows.push(row);
  }

  return rows.filter((values) => values.some((value) => value.trim().length > 0));
}

function normalizePath(pathValue) {
  const raw = String(pathValue ?? "").trim();
  if (!raw) return "";
  const noHost = raw.replace(/^https?:\/\/[^/]+/i, "");
  const withLeadingSlash = noHost.startsWith("/") ? noHost : `/${noHost}`;
  return withLeadingSlash.replace(/\/{2,}/g, "/");
}

function toMarkdownTable(rows) {
  if (rows.length === 0) {
    return "_None_";
  }
  const header = "| old_url | new_url | reason |";
  const divider = "|---|---|---|";
  const lines = rows.map(
    (row) => `| \`${row.old_url}\` | \`${row.new_url}\` | ${row.reason} |`,
  );
  return [header, divider, ...lines].join("\n");
}

const csvRaw = readFileSync(csvPath, "utf8");
const csvRows = parseCsv(csvRaw);
if (csvRows.length < 2) {
  throw new Error(`No redirect rows found in ${csvPath}`);
}

const headers = csvRows[0].map((value) => value.trim());
const columnIndex = Object.fromEntries(headers.map((name, index) => [name, index]));
for (const required of ["old_url", "new_url", "status_code"]) {
  if (!(required in columnIndex)) {
    throw new Error(`Missing required column "${required}" in ${csvPath}`);
  }
}

const outputLines = [
  "# Auto-generated by scripts/phase5/generate-cloudflare-redirects.mjs",
  "# Source: docs/redirects.csv",
  "# Media offload to R2 custom domain (keep this above legacy redirects).",
  "/uploads/* https://images.nomadicyear.com/uploads/:splat 302",
];

const seenOldPaths = new Map();
const duplicates = [];
let validCount = 0;

for (const rowValues of csvRows.slice(1)) {
  const oldPath = normalizePath(rowValues[columnIndex.old_url]);
  const newPath = normalizePath(rowValues[columnIndex.new_url]);
  const statusRaw = String(rowValues[columnIndex.status_code] ?? "").trim();
  const statusCode = Number(statusRaw || "301");
  if (!oldPath || !newPath) {
    continue;
  }

  if (![301, 302, 307, 308].includes(statusCode)) {
    throw new Error(`Unsupported status code "${statusRaw}" for ${oldPath} -> ${newPath}`);
  }

  const duplicateExisting = seenOldPaths.get(oldPath);
  if (duplicateExisting && duplicateExisting !== `${newPath}|${statusCode}`) {
    duplicates.push({
      old_url: oldPath,
      new_url: newPath,
      reason: `conflicts with existing mapping ${duplicateExisting}`,
    });
    continue;
  }

  seenOldPaths.set(oldPath, `${newPath}|${statusCode}`);
  outputLines.push(`${oldPath} ${newPath} ${statusCode}`);
  validCount += 1;
}

writeFileSync(redirectsOutPath, `${outputLines.join("\n")}\n`, "utf8");

const report = {
  generatedAt: new Date().toISOString(),
  sourceCsvPath: csvPath,
  outputRedirectsPath: redirectsOutPath,
  totalCsvRows: Math.max(0, csvRows.length - 1),
  validRedirectRows: validCount,
  duplicateConflicts: duplicates.length,
  duplicateConflictRows: duplicates,
};

writeFileSync(reportJsonPath, `${JSON.stringify(report, null, 2)}\n`, "utf8");

const md = [
  "# Phase 5 Redirect Report",
  "",
  `Generated: ${report.generatedAt}`,
  "",
  "## Summary",
  `- Source rows: ${report.totalCsvRows}`,
  `- Redirects written: ${report.validRedirectRows}`,
  `- Duplicate conflicts: ${report.duplicateConflicts}`,
  "",
  "## Duplicate Conflicts",
  toMarkdownTable(duplicates),
  "",
];
writeFileSync(reportMdPath, md.join("\n"), "utf8");

console.log(`Generated ${validCount} redirects at ${redirectsOutPath}`);
console.log(`Report: ${reportJsonPath} and ${reportMdPath}`);
